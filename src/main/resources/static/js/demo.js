initleishanMap();
function initleishanMap(){
	//初始化函数，获取jion中的数据，作为散点图中的数据，模拟数据，
	var convertdata=function (data){
		var res=[];
		for(var i=1;i<data.features.length;i++){
			var name=data.features[i].properties.name;
			var address=data.features[i].cp;
			if(name){
				res.push({
					name:name,
					value:address.concat(name)
				})
			}
		}
		return res;
	}
	var dom = document.getElementById("left-content2");
	myChart=echarts.init(dom);
	myChart.showLoading();
	myChart.hideLoading();
	//注册地图
	echarts.registerMap('LS',leishan);
	var option={
		//自定义提示框的内容，固定位置，
		tooltip:{
			formatter:function(params){
				return temps(params.name);
			}
		},
		//初始化散点图的坐标系，就是底图
		geo:{
			name:'汉东省',
			type:'map',
			zoom:1.1,
			roam:true,
			//地图名称
			map:'LS',
			//如果将此处的label注释掉，地图中将不会显示市级名称
			label:{
				show:true
			},
			itemStyle:{
				normal:{
					//初始化地图背景颜色
					areaColor:'#6b90c7'
				},
				emphasis:{
					//当鼠标移动此处时的地图背景颜色
					areaColor:'#6CC7FF'
				}
			}
		},
		series:[{
			name:'city',
			type:'effectScatter',
			//地图坐标系，
			coordinateSystem:'geo',
			//不显示当前的散点图的信息，用label中的formatter不可识别html标签，tooltip可识别
			label:{
				normal:{
					show:false
				}
			},
			itemStyle:{
				normal:{
					color:'#ffff00'//颜色
					
				},
				emphasis:{
					label:{
						show:false
					},
					areaColor:'#6CC7FF'
				},
				
			},
			markArea: {
                    itemStyle: {
                        normal: {
                            color: ['#7FFFD4']
                        }
                    }
            },
			//获取数据
			data:convertdata(leishan)
		}]
	}
	myChart.setOption(option);
	myChart.on('click', function (params) {//点击事件
		newTab("标杆供电所信息",getRootPath_dc()+"/ypjc/homepage/jsp/xjGds.jsp");
	});
}


function contains(arrays, obj) {
    var i = arrays.length;
   for(var a=0;a < i;a++) {
        if (arrays[a].ORG_NAME == obj) {
            return a;
        }
    }
    return false;
}

